from pydantic import BaseModel, Field
from typing import Dict, Any, Optional, List
from datetime import datetime


class ExperimentBase(BaseModel):
    name: str = Field(..., example="XGBoost Tuning", description="Name of the machine learning experiment")
    model_type: str = Field(..., example="xgboost", description="Type of ML model (e.g., random_forest, xgboost, svm)")
    hyperparameters: Dict[str, Any] = Field(default_factory=dict, description="Dictionary of model hyperparameters")
    dataset_name: str = Field(..., example="california_housing", description="Name/path of the training dataset")
    target_column: str = Field(..., example="median_house_value", description="Target variable name for prediction")


class ExperimentCreate(ExperimentBase):
    pass


class ExperimentUpdate(BaseModel):
    status: str = Field(..., example="running", description="Status of execution (pending, running, completed, failed)")
    metrics: Optional[Dict[str, float]] = Field(None, description="Logged evaluation metrics (e.g. RMSE, R2, accuracy)")
    artifacts: Optional[Dict[str, str]] = Field(None, description="Paths to saved model binaries, charts, etc.")
    finished_at: Optional[datetime] = Field(None, description="Timestamp of completion")
    error_message: Optional[str] = Field(None, description="Error trace if the experiment failed")


class ExperimentResponse(ExperimentBase):
    id: str = Field(..., description="Unique run identifier generated by backend")
    status: str = Field("pending", description="Current status of the experiment")
    metrics: Dict[str, float] = Field(default_factory=dict, description="Key-value metrics dictionary")
    artifacts: Dict[str, str] = Field(default_factory=dict, description="Key-value model artifact links")
    created_at: datetime = Field(default_factory=datetime.utcnow, description="Creation timestamp")
    finished_at: Optional[datetime] = Field(None, description="Completion timestamp")
    duration: Optional[str] = Field(None, description="Formated run execution time")
    logs: List[str] = Field(default_factory=list, description="Execution logging outputs")

    class Config:
        from_attributes = True
